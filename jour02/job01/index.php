    // si la valeur de la variable $ch est strictement egal à 42
    // affiche la valeur en gras et sous-ligné
    if ($ch == 42){
        echo "<b><u> $ch </b></u><br/>";
    }
    // si non affiche la valeur normal de $ch
    else {
        echo "$ch<br/>";
    }
}

